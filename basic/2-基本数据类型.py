# int 整数类型
print(1)  # 输出整数 1
print(type(1))  # 输出整数类型 <class 'int'>
# float 浮点数类型（小数）
print(1.0)  # 输出浮点数 1.0
print(type(1.0))  # 输出浮点数类型 <class 'float'>
# bool 布尔类型 （True or False）
print(True)  # 输出布尔值 True
print(type(True))  # 输出布尔类型 <class 'bool'>
print(False)  # 输出布尔值 False
print(type(False))  # 输出布尔类型 <class 'bool'>
# str 字符串类型 （单引号或双引号括起来的字符） "1"
print("1a")  # 输出字符串 "1"
print(type("1"))  # 输出字符串类型 <class 'str'>
