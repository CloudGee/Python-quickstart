# 加法运算符
print(1 + 2)  # 输出 3
# 减法运算符
print(2.0 - 1)  # 输出 1.0，浮点型优先级高
# 乘法运算符
print(2 * 3)  # 输出 6
# 除法运算符
print(6 / 3)  # 输出 2.0，结果为浮点数

# 幂运算符
print(2 ** 3)  # 输出 8，2 的 3 次方

# 整除运算符
print(7 // 3)  # 输出 2，整除结果为整数

# 取余运算符，模运算符
print(7 % 3)  # 输出 1，7 除以 3 的余数

# 左移运算符
print(1 << 2)  # 输出 4，二进制左移操作，相当于乘以 2 的 2 次方
# 1的二进制是 0001，左移两位变成 0100，即十进制的 4
# a << n 等价于 a * (2 ** n)

# 右移运算符
print(4 >> 2)  # 输出 1，二进制右移操作，相当于除以 2 的 2 次方
# 4的二进制是 0100，右移两位变成 0001，即十进制的 1
# a >> n 等价于 a // (2 ** n)

# 按位与运算符
print(5 & 3)  # 输出 1，按位与操作
# 与代表两个二进制位都为 1 时结果才为 1，否则为 0，类似二进制乘法运算
# 5的二进制是 0101，3的二进制是 0011，按位与结果是 0001，即十进制的 1

# 按位或运算符
print(5 | 3)  # 输出 7，按位或操作
# 或代表两个二进制位只要有一个为 1 时结果就为 1，只有两个操作数都为0，答案才是0
# 5的二进制是 0101，3的二进制是 0011，按位或结果是 0111，即十进制的 7

# 按位异或运算符
print(5 ^ 3)  # 输出 6，按位异或操作
# 异或代表两个二进制位相同为 0，不同为 1
# 5的二进制是 0101，3的二进制是 0011，按位异或结果是 0110，即十进制的 6

# 小于运算符
print(1 < 2)  # 输出 True，1 小于 2
# 大于运算符
print(2 > 3)  # 输出 False
# 小于等于运算符
print(1 <= 1)  # 输出 True，1 小于等于 1
# 大于等于运算符
print(3 >= 2)  # 输出 True
# 等于运算符
print(1 == 1)  # 输出 True，1 等于 1
# 不等于运算符
print(type(2 != 2))  # 输出 False
